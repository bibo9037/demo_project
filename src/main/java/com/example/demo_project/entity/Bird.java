package com.example.demo_project.entity;

public class Bird {				
	private String name;
	private int age;
	
	public Bird() {					//空白建構方法
		
	}
	
	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}

}
